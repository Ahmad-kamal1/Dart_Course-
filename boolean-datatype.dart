// Boolean datatype in Dart.

void main() {
  int num1 = 10; // first variable.
  int num2 = 30; // second variable.
  int sum = num1 + num2; // sum of first and second variable.
  bool result =
      sum >
      50; // boolean datatype to check if sum is greater than 50 it will return true or false.

  print(result);
}
